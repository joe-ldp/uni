DROP TABLE CUST;
DROP TABLE CUSTACC;
DROP TABLE ACC;

CREATE TABLE CUST
(
    REFNO VARCHAR2(4),
    NAME VARCHAR2(15),
    ADDRESS VARCHAR2(30),
    AREA VARCHAR2(20)
);

CREATE TABLE CUSTACC
(
    REFNO VARCHAR2(4),
    ACCNO NUMBER(9)
);

CREATE TABLE ACC
(
    ACCNO NUMBER(9),
    BALANCE NUMBER,
    BRANCH VARCHAR2(20),
    OPENED DATE,
    BONUS NUMBER
);

